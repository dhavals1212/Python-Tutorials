numbers = (6, 7, 8)
#Tuples are used for immutable operations.
#Unlike the lists, tuple cannot be added or changed.

print(numbers.count(7))
#Just like in lists, here as well, the count method will return times the number is repeated there.

print(numbers.index(8))
#Same as in the lists, here as well the numbers given as inputs will show the index as output.